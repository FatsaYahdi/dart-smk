void main() {
  var rank = 'SS'; // SS S A B C D
  switch (rank) {
    case 'SS':
      print('SS');
      break;
    case 'S':
      print('S');
      break;
    case 'A':
      print('A');
      break;
    case 'B':
      print('B');
      break;
    case 'C':
      print('C');
      break;
    case 'D':
      print('D');
      break;
    default:
      print('invalid');
  }
}
